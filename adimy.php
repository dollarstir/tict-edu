          <h5><strong>INTRODUCTION:</strong></h5>
          The aim of this course is to introduce students to the purpose and power of theories as they provide insight into marriage and family life. Students will be guided to evaluate both the strengths and challenges of these theoretical perspectives and also learn how to actually use those theories to improve various aspects of marriage and family dynamics.
          This course will take an in-depth look at various family theories, including systems theory, exchange theory, family development theory, symbolic interactionism, and others, and explore some of the assumptions associated with each one. It will examine family processes such as those related to power, communication, dysfunctions and addictions, rules and patterns of interaction, distance regulation, and family rituals through the lens of each theory's unique perspective.
